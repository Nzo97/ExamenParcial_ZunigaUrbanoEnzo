package modelo;

/**
 *
 * @author enzol
 */
public class cliente {
    private float dinero;
    private int puntos;

    public cliente(float dinero, int puntos) {
        this.dinero = dinero;
        this.puntos = puntos;
    }

    public float getDinero() {
        return dinero;
    }

    public void setDinero(float dinero) {
        this.dinero = dinero;
    }

    public int getPuntos() {
        return puntos;
    }

    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }
    
}
